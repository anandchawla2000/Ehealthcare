package beans;

public class Chat {
int sendid;
int recid;
String msg;
public int getSendid() {
	return sendid;
}
public void setSendid(int sendid) {
	this.sendid = sendid;
}
public int getRecid() {
	return recid;
}
public void setRecid(int recid) {
	this.recid = recid;
}
public String getMsg() {
	return msg;
}
public void setMsg(String msg) {
	this.msg = msg;
}
}
